            markup.row(types.InlineKeyboardButton(text=f'‚¨Ö', callback_data=f'back'),
                       types.InlineKeyboardButton(text=f'{pagen + 1}/{len(lst_pagen_value)}', callback_data=f' '),
                       types.InlineKeyboardButton(text=f'üîô –ù–∞–∑–∞–¥', callback_data=f'back_menue'))
